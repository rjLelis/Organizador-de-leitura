    //MÃ©todo de cadastrar um novo leitor
    public void cadastrarLeitor(){
        if(txtNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Favor informar o nome", "Erro", JOptionPane.ERROR_MESSAGE);
        } else if (txtApelido.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Favos informar o apelido", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int idade = Integer.parseInt((String) cbbIdade.getSelectedItem());
                conn = Dados.conectarComParametros();
                leitor.setNome(txtNome.getText().trim());
                leitor.setData_nascimento(idade);
                leitor.setApelido(txtApelido.getText());
                validadorDeLeitor.cadastrarLeitor(leitor);
                JOptionPane.showMessageDialog(this,"Leitor cadastrado com sucesso!","Sucesso!",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Dados.desconectar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }

